  <p>En 1978 España tenía una tensión enorme: por un lado, décadas de centralismo absoluto que habían ignorado las identidades regionales; por otro, movimientos nacionalistas fuertes en el País Vasco y Cataluña que pedían autogobierno. El Estado federal al estilo alemán o americano era políticamente inviable. El Estado centralista tampoco era aceptable.</p>
